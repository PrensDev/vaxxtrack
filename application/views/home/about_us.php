        <p>
        Contact tracing is a crucial public health strategy for COVID-19, 
        especially since many nations are phasing out stay-at-home programs and adjusting community-level physical distancing measures. 
        The goal of contact tracing is to break transmission chains by notifying the establishment owners and health officials who have come into touch 
        with an infected person that they are at a higher risk of infection and how to protect themselves from spreading the sickness to others.
        </p>

        <p>
        On the other hand, as COVID-19 vaccines are introduced, national and subnational governments, the general public, 
        civil society organizations, the media, and regional and global immunization partners, 
        and even local establishments will place a high demand on data from national and subnational public health agencies.
        </p>

        <p>
        This project is designed and made efficiently to facilitate a contract tracing and vaccination monitoring system 
        to automate its operations of generating reports, keeping records and storing them in the form of a large and user friendly 
        database to further facilitate easy access to the personnels. The contact tracing system is the first line of defense in achieving speedy 
        and successful contact tracing of confirmed patients, as well as preventing large-scale community infections.
        </p>

